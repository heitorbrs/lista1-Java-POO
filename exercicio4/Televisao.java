public class Televisao {

    private int canal=1; 
    private int volume=0;

    public void setCanal(int canal) {
        this.canal = canal;
    }

    public void setVolume(int volume) {
        this.volume = volume;
    }
    
    public int getCanal() {
        return canal;
    }

    public int getVolume() {
        return volume;
    }
}
